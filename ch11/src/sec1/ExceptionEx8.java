package sec1;

public class ExceptionEx8 {

	public static void main(String[] args) {
		/*
		 Runtime Exception : 실행을 시켰을 때 나타나는 예외
		 
		 ClassCastException : 형변환 예외
		 ArithmethicException
		 NullPointerException
		 NumberFormatException
		 InderOutOfBoundsException
		 NoSuchElementException : 요구한 요소 (함수)가 존재하지 않는 경우 발생
		 IllegalArgumentException : 메소드에 부적절한 파라미터 값을 넣어서 호출할 경우 발생
		 */
		
		/*
		 General Exception : 실행과 관계없는 무조건 처리해야하는 예외
		 
		 ClassNotFoundException : 해당 클래스가 존재하지 않는 경우 발생
		 FileNotFoundException : 파일이 존재하지 않는 경우 발생
		 IOException : 데이터를 읽거나 쓸 때 발생하는 예외
		 InterruptedException : 인터럽트 되었을 때 발생하는 예외
		 NoSuchFieldException : 클래스에 명시한 필드가 포함되지 않은 경우
		 NoSuchMethodException : 클래스에 명시한 메소드가 포함되지 않은 경우
		 
		 */
	}
}
